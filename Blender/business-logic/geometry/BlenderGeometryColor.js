
export default class BlenderGeometryColor {

    setRed(red) {
        this.red = red;
    }

    getRed() {
        return this.red;
    }

    setGreen(green) {
        this.green = green;
    }

    getGreen() {
        return this.green;
    }

    setBlue(blue) {
        this.blue = blue;
    }

    getBlue() {
        return this.blue;
    }

    setRedGreenBlue(red, green, blue) {
        this.red = red;
        this.green = green;
        this.blue = blue;
    }
   
}